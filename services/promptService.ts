const getAmmoStr = (character: Character): string => {
  const ammoSummary: Record<string, { count: number, totalAmount: number, unitLabel: string }> = {};
  [...(character.equipment || []), ...(character.items || [])].forEach(itemStr => {
    if (!itemStr) return;
    const match = itemStr.match(/^(.*?)\s*\(x(\d+)\)$/);
    const name = match ? match[1].trim() : itemStr.trim();
    const count = match ? parseInt(match[2], 10) : 1;
    
    const isAmmo = name.includes('弹') || name.includes('箭矢') || name.includes('电池') || name.includes('Energy Pack');
    const isConsumable = name.includes('食物') || name.includes('饮水') || name.includes('补给');
    
    if (isAmmo || isConsumable) {
      let amountMultiplier = 1;
      let unitLabel = isAmmo ? '发' : '份';
      const desc = lookupKnowledge(name, 'items') || lookupKnowledge(name, 'weapons') || lookupKnowledge(name, 'armor');
      if (desc) {
         const m = desc.match(/(\d+)\s*(发|支|枚|次|日|份)/);
         if (m) {
           amountMultiplier = parseInt(m[1], 10);
           unitLabel = m[2];
         } else if (desc.includes('单枚') || desc.includes('单份')) {
           amountMultiplier = 1;
         }
      }
      
      let baseName = name.replace(/弹匣|燃料罐|罐|电容器|高容量|弹夹|弹鼓|弹药箱|配给|淡水/g, '').trim();
      if (!baseName) baseName = name;

      if (!ammoSummary[baseName]) {
        ammoSummary[baseName] = { count: 0, totalAmount: 0, unitLabel };
      }
      ammoSummary[baseName].count += count;
      ammoSummary[baseName].totalAmount += (count * amountMultiplier);
    }
  });

  return Object.entries(ammoSummary).length > 0 
    ? Object.entries(ammoSummary).map(([n, data]) => {
        if (data.totalAmount === data.count) {
           return `${n}:${data.count}${data.unitLabel}`;
        }
        return `${n}:${data.count}盒/组(共${data.totalAmount}${data.unitLabel})`;
      }).join('; ') + ' 【再次警告：物品栏里的"xN"是盒数/弹匣数，不是单发数！绝对不要因为打了一枪就扣掉 x1 盒或者判定子弹用完！扣除时只允许在换弹匣时扣除1盒！】'
    : '无关键消耗品记录';
};

