/**
 * Helper to format a list of names with their descriptions from the knowledge base
 */
const formatListWithKnowledge = (list: any, type: 'traits' | 'skills' | 'weapons' | 'armor' | 'items' | 'gear'): string => {
  if (!list) return '无';
  
  // Handle TraitData[] or Skill[] objects
  if (Array.isArray(list) && typeof list[0] === 'object') {
    return list.map(item => {
      const name = item.name;
      const lookupName = name.replace(/^【|】$/g, '');
      const levelStr = item.level !== undefined ? `[Lv.${item.level}]` : '';
      const desc = item.desc || item.description || (type === 'traits' ? lookupKnowledge(lookupName, 'traits') : lookupKnowledge(lookupName, 'skills'));
      return desc ? `${name}${levelStr}: ${desc}` : `${name}${levelStr}`;
    }).join('; ');
  }

  // Handle string lists
  const items = Array.isArray(list) ? list : list.toString().split(/[;，,]/).map((s: string) => s.trim()).filter(Boolean);
  return items.map((name: string) => {
    const match = name.match(/^(.*?)\s*\(x\d+\)$/);
    let lookupName = match ? match[1].trim() : name;
    lookupName = lookupName.replace(/^【|】$/g, '');
    
    let desc = null;
    if (type === 'gear') {
      desc = lookupKnowledge(lookupName, 'weapons') || lookupKnowledge(lookupName, 'armor') || lookupKnowledge(lookupName, 'items');
    } else {
      const lookupType = type === 'traits' ? 'traits' : type === 'skills' ? 'skills' : type === 'items' ? 'items' : 'items';
      desc = lookupKnowledge(lookupName, lookupType as any);
    }
    return desc ? `${name}: ${desc}` : name;
  }).join('; ');
};

