/**
 * Helper to get description from knowledge base
 */
const lookupKnowledge = (name: string, type: 'traits' | 'skills' | 'weapons' | 'armor' | 'items'): string | null => {
  if (!name || !knowledgeSummary) return null;
  const list = (knowledgeSummary as any)[type];
  if (!list) return null;
  
  const entry = list.find((item: any) => 
    item.name.toLowerCase() === name.toLowerCase() || 
    (item.name.includes('|') && item.name.split('|').some((n: string) => n.trim().toLowerCase() === name.toLowerCase()))
  );
  
  return entry ? entry.description || entry.desc : null;
};

const getLexicon = (character?: Character) => {
  if (!character) return ALIGNMENT_LEXICON.IMPERIAL;
  if (character.alignment?.includes('混沌') || character.lineage?.includes('混沌') || character.lineage?.includes('变节')) {
    return ALIGNMENT_LEXICON.CHAOS;
  }
  if (character.lineage?.includes('泰伦') || character.lineage?.includes('虫群')) {
    return ALIGNMENT_LEXICON.XENOS;
  }
  return ALIGNMENT_LEXICON.IMPERIAL;
};

